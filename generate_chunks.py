def escape_backtick(text):
    """转义 JS 模板字符串中的反引号和 ${ """
    text = text.replace('\\', '\\\\')
    text = text.replace('`', '\\`')
    text = text.replace('${', '\\${')
    return text

def clean_page_text(raw_text):
    """清理页面文本：去掉多余空行，整理格式"""
    lines = raw_text.split('\n')
    cleaned = []
    for line in lines:
        line = line.strip()
        if line:
            cleaned.append(line)
    return '\n'.join(cleaned)

def text_to_html(text):
    """将纯文本转换为 HTML 格式"""
    lines = text.split('\n')
    html_parts = []
    in_list = False
    
    for line in lines:
        line = line.strip()
        if not line:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            continue
        
        # 检测标题层级（数字开头的章节）
        if re.match(r'^\d+\.\d+\s+', line) or re.match(r'^[一二三四五六七八九十]+[、．.]\s*', line):
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            escaped = html.escape(line)
            html_parts.append(f'<h3>{escaped}</h3>')
        elif re.match(r'^\d+\.\s+\S', line) and len(line) < 60:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            escaped = html.escape(line)
            html_parts.append(f'<h3>{escaped}</h3>')
        # 检测表格标题行
        elif re.match(r'^表格$|^图表$|^表\d+|^图\d+', line):
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            escaped = html.escape(line)
            html_parts.append(f'<p><strong>{escaped}</strong></p>')
        # 检测列表项（• · - * 开头）
        elif re.match(r'^[•·\-\*]\s+', line) or re.match(r'^\d+[.）)]\s+.{5,}', line):
            if not in_list:
                html_parts.append('<ul>')
                in_list = True
            content = re.sub(r'^[•·\-\*\d）)\.]\s+', '', line)
            escaped = html.escape(content)
            html_parts.append(f'<li>{escaped}</li>')
        else:
            if in_list:
                html_parts.append('</ul>')
                in_list = False
            escaped = html.escape(line)
            html_parts.append(f'<p>{escaped}</p>')
    
    if in_list:
        html_parts.append('</ul>')
    
    return '\n'.join(html_parts)

def extract_section_title(page_text, page_num):
    """从页面文本提取章节标题（取最能代表内容的行）"""
    lines = [l.strip() for l in page_text.split('\n') if l.strip()]
    if not lines:
        return f'第 {page_num} 页'
    
    # 常见的页眉/页脚噪音词，遇到这些跳过
    NOISE_PATTERNS = [
        r'^Prompt Engineering\s+Prompt Engineering$',
        r'^小艺深度研究$',
        r'^内容由\s*AI\s*生成',
        r'^\d+$',  # 纯数字页码
        r'^Generated by',
        r'^www\.',
        r'^http',
        r'^February \d+',
        r'^©',
        r'^kimi',
    ]
    
    def is_noise(line):
        for pattern in NOISE_PATTERNS:
            if re.match(pattern, line, re.IGNORECASE):
                return True
        return False
    
    # 优先找章节标题行（有中文章节编号）
    for line in lines:
        if is_noise(line):
            continue
        # 中文章节号模式
        if re.match(r'^[一二三四五六七八九十百]+[章节、，。]', line) or \
           re.match(r'^\d+\.\s+[\u4e00-\u9fff]', line) or \
           re.match(r'^第[一二三四五六七八九十百\d]+[章节部]', line):
            if len(line) <= 60:
                return line
    
    # 找第一个非噪音、有意义的行
    for line in lines[:10]:
        if is_noise(line):
            continue
        # 跳过太短（< 4字符）和太长（> 60字符）的行
        if len(line) < 4 or len(line) > 60:
            continue
        # 跳过全是英文且重复的行（如 "Prompt Engineering Prompt Engineering"）
        words = line.split()
        if len(words) >= 2 and len(words) % 2 == 0:
            half = words[:len(words)//2]
            if half == words[len(words)//2:]:
                continue  # 重复行
        # 去掉行号或特殊字符开头
        clean = re.sub(r'^[\d\s\-=\.]+', '', line).strip()
        if clean and len(clean) >= 3:
            return clean[:50]
    
    # 最后备用：返回第一行截断
    for line in lines:
        if not is_noise(line) and len(line) > 2:
            return line[:40]
    
    return f'第 {page_num} 页'

def pages_to_sections(pages):
    """将页面列表转换为 sections 数组，每个页面为一个 section"""
    sections = []
    for i, (page_num, page_text) in enumerate(pages):
        cleaned = clean_page_text(page_text)
        if not cleaned:
            continue
        
        title = extract_section_title(cleaned, page_num)
        html_content = text_to_html(cleaned)
        
        sections.append({
            'title': title,
            'level': 1,
            'content_raw': html_content,
            'page_num': page_num
        })
    
    return sections

def group_pages_into_sections(pages, target_sections_per_chunk=5):
    """将多个页面分组成 sections（每3页一个section）"""
    sections = []
    group_size = 3  # 每3页合并为一个section
    
    for i in range(0, len(pages), group_size):
        group = pages[i:i+group_size]
        if not group:
            continue
        
        first_page_num = group[0][0]
        last_page_num = group[-1][0]
        
        # 合并文本
        combined_text = '\n\n'.join([clean_page_text(text) for _, text in group if clean_page_text(text)])
        if not combined_text:
            continue
        
        # 取第一页的标题
        title = extract_section_title(group[0][1], first_page_num)
        page_range_str = f'第 {first_page_num}-{last_page_num} 页' if first_page_num != last_page_num else f'第 {first_page_num} 页'
        
        html_content = text_to_html(combined_text)
        
        sections.append({
            'title': title,
            'level': 1,
            'content_raw': html_content,
            'page_num': first_page_num,
            'page_range': f'{first_page_num}-{last_page_num}'
        })
    
    return sections

def generate_chunk_js(report_id, chunk_index, page_range_str, sections):
    """生成 chunk.js 文件内容"""
    report_id_js = report_id.replace('-', '_')
    
    sections_js = []
    for sec in sections:
        title_escaped = sec['title'].replace('\\', '\\\\').replace("'", "\\'")
        content_escaped = escape_backtick(sec['content_raw'])
        
        sec_js = f"""    {{
      title: '{title_escaped}',
      level: {sec['level']},
      content: `{content_escaped}`
    }}"""
        sections_js.append(sec_js)
    
    sections_str = ',\n'.join(sections_js)
    
    return f"""// 报告: {report_id} - 第{chunk_index}块
// 页数: {page_range_str}

window.REPORT_CHUNK_{report_id_js}_{chunk_index} = {{
  reportId: "{report_id}",
  chunkIndex: {chunk_index},
  pageRange: "{page_range_str}",
  sections: [
{sections_str}
  ]
}};
"""

def generate_manifest(report_id, total_pages, chunks_info):
    """生成 manifest.json 文件内容"""
    chunks = []
    for ci in chunks_info:
        chunks.append({
            "index": ci['index'],
            "file": f"chunk-{ci['index']}.js",
            "sections": ci['section_titles'],
            "pageRange": ci['page_range'],
            "size": ci['size']
        })
    
    manifest = {
        "id": report_id,
        "title": "",  # Will be filled in
        "totalPages": total_pages,
        "totalChunks": len(chunks),
        "chunks": chunks
    }
    return manifest

