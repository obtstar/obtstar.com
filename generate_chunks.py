def parse_pdf_content(content_text):
    """解析 PDF 内容文本，返回 {filename: [(page_num, page_text), ...]}"""
    result = {}
    
    # 按文件分割
    parts = re.split(r'={80}\nFILE: (.+?)\n={80}', content_text)
    
    for i in range(1, len(parts), 2):
        filename = parts[i].strip()
        file_content = parts[i+1] if i+1 < len(parts) else ''
        
        # 按页分割
        page_splits = re.split(r'\n--- Page (\d+) ---\n', file_content)
        # page_splits[0] is header ([Total pages: N])
        
        pages = []
        for j in range(1, len(page_splits), 2):
            page_num = int(page_splits[j])
            page_text = page_splits[j+1] if j+1 < len(page_splits) else ''
            pages.append((page_num, page_text))
        
        result[filename] = pages
    
    return result

def process_report(report_id, pages, output_dir):
    """处理单个报告，生成所有 chunk.js 和 manifest.json"""
    os.makedirs(output_dir, exist_ok=True)
    
    total_pages = len(pages)
    
    # 分 chunk（每 PAGES_PER_CHUNK 页一个 chunk）
    chunk_infos = []
    
    for chunk_idx, start in enumerate(range(0, total_pages, PAGES_PER_CHUNK)):
        chunk_pages = pages[start:start + PAGES_PER_CHUNK]
        
        first_page = chunk_pages[0][0]
        last_page = chunk_pages[-1][0]
        page_range_str = f'{first_page}-{last_page}'
        
        # 将页面分组为 sections（每3页一组）
        sections = group_pages_into_sections(chunk_pages)
        
        if not sections:
            continue
        
        # 生成 chunk.js
        chunk_js = generate_chunk_js(report_id, chunk_idx, page_range_str, sections)
        chunk_file = os.path.join(output_dir, f'chunk-{chunk_idx}.js')
        with open(chunk_file, 'w', encoding='utf-8') as f:
            f.write(chunk_js)
        
        file_size = os.path.getsize(chunk_file)
        size_str = f'约{file_size // 1024}KB'
        
        section_titles = [s['title'] for s in sections]
        chunk_infos.append({
            'index': chunk_idx,
            'page_range': page_range_str,
            'section_titles': section_titles,
            'size': size_str
        })
        
        print(f'  ✅ chunk-{chunk_idx}.js: 页{page_range_str}, {len(sections)}个section, {size_str}')
    
    # 生成 manifest.json
    manifest = generate_manifest(report_id, total_pages, chunk_infos)
    manifest['title'] = REPORT_TITLES.get(report_id, report_id)
    
    manifest_file = os.path.join(output_dir, 'manifest.json')
    with open(manifest_file, 'w', encoding='utf-8') as f:
        json.dump(manifest, f, ensure_ascii=False, indent=2)
    
    print(f'  ✅ manifest.json: {total_pages}页, {len(chunk_infos)}个chunk')
    return chunk_infos

